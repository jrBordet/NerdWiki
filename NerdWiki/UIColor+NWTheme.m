#import "UIColor+NWTheme.h"

@implementation UIColor (NWTheme)

+ (UIColor *)bgColor {
    return [self colorWithRed:32.0f/255.0f
                       green:32.0f/255.0f
                        blue:32.0f/255.0f
                       alpha:1.0f];
}

@end
